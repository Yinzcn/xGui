const TCHAR *
wMsgToStr(UINT uMsg)
{
    const TCHAR * v = kVal_Get(wMsg_List, uMsg);
    if (v) return v;
    {
        static TCHAR buff[128];
        _sntprintf(buff, sizeof(buff), _T("*******************WM(0x%08x)"), uMsg);
        return buff;
    }
}
